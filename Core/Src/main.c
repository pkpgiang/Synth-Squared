int linear_interpolate(float index){
	int index_floor = floor(index);
	int index_next = (index_floor + 1) % WAVETABLE_SIZE;
	float index_next_weight = index - index_floor;
	float index_floor_weight = 1.0 - index_next_weight;

	return floor(index_floor_weight*wavetable[index_floor] + index_next_weight*wavetable[index_next]);
}

